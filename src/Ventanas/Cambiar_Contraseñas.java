    private void btnactualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnactualizarActionPerformed
    String dni = txtdni.getText().trim();
    String nombre = txtnombre.getText().trim();
    String apellido = txtapellido.getText().trim();
    String correo = txtcorreo.getText().trim();
    String usuario = txtusuario.getText().trim().replace(",", "");
    String contraseña = txtcontraseña.getText().trim();
    String rol = txtrol.getText().trim();

    if (usuario.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Primero indique o busque el nombre de usuario a buscar.");
        return;
    }

    try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/gestion_mar", "root", "12345678")) {

        String sql = "UPDATE usuarios SET dni=?, nombre=?, apellido=?, correo=?, contraseña=?, rol=? WHERE usuario=?";
        try (PreparedStatement ps = conn.prepareStatement(sql)) {
            ps.setString(1, dni);
            ps.setString(2, nombre);
            ps.setString(3, apellido);
            ps.setString(4, correo);
            ps.setString(5, contraseña);
            ps.setString(6, rol);
            ps.setString(7, usuario);

            int resultado = ps.executeUpdate();

            if (resultado > 0) {
                JOptionPane.showMessageDialog(this, "Datos actualizados correctamente.");
                // Limpiar campos después de actualizar
                txtdni.setText("");
                txtnombre.setText("");
                txtapellido.setText("");
                txtcorreo.setText("");
                txtusuario.setText("");
                txtcontraseña.setText("");
                txtrol.setText("");
                txtbuscar.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "No se encontró ningun usuario.");
            }
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error al actualizar: " + e.getMessage());
    }

    }//GEN-LAST:event_btnactualizarActionPerformed

