    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed
      String usuario = txtbuscar.getText().trim(); 

    if (usuario.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Ingrese el nombre de usuario para buscar.");
        return;
    }

    try {
        // Conexión a la base de datos
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/gestion_mar", "root", "12345678");

        // Consulta SQL sin distinguir mayúsculas/minúsculas
        String sql = "SELECT * FROM usuarios WHERE LOWER(usuario) = LOWER(?)";
        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setString(1, usuario);

        ResultSet rs = ps.executeQuery();

        if (rs.next()) {
            // Mostrar los resultados en los campos correspondientes
            txtdni.setText(rs.getString("dni"));
            txtnombre.setText(rs.getString("nombre"));
            txtapellido.setText(rs.getString("apellido"));
            txtcorreo.setText(rs.getString("correo"));
            txtusuario.setText(rs.getString("usuario"));
            txtcontraseña.setText(rs.getString("contraseña"));
            txtrol.setText(rs.getString("rol"));
            
        } else {
            JOptionPane.showMessageDialog(this, "No se encontró ningun registro disponible con ese nombre de usuario .");
        }

        rs.close();
        ps.close();
        conn.close();

    } catch (SQLException e) {
    JOptionPane.showMessageDialog(this, "Error al buscar: " + e.getMessage());
}

    }//GEN-LAST:event_btnbuscarActionPerformed

